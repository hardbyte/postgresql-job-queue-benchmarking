def _load_rows(raw_csv: Path) -> list[dict]:
    with raw_csv.open("r", newline="") as fh:
        return list(csv.DictReader(fh))


def _system_meta(systems: list[str]) -> dict[str, dict[str, str]]:
    out: dict[str, dict[str, str]] = {}
    for system in systems:
        try:
            manifest = AdapterManifest.load(Path(__file__).resolve().parent.parent / f"{system}-bench")
            out[system] = {
                "family": manifest.family,
                "display_name": manifest.display_name,
            }
        except Exception:
            out[system] = {"family": system, "display_name": system}
    return out


def _timeline_series(rows: list[dict], systems: list[str]) -> dict[str, dict[str, list[dict]]]:
    """Aggregate raw rows into per-metric, per-system time series."""
    out: dict[str, dict[str, list[dict]]] = {}
    for metric in DEFAULT_TIMELINE_METRICS:
        out[metric] = {}
        for system in systems:
            selected = [
                r
                for r in rows
                if r["system"] == system and r["metric"] == metric
            ]
            if not selected:
                continue

            # Adapter metrics are one value per tick. Table metrics such as
            # n_dead_tup need summing across subjects for each timestamp.
            if metric == "n_dead_tup":
                per_elapsed: dict[tuple[float, str, str], float] = {}
                for row in selected:
                    if row["subject_kind"] != "table":
                        continue
                    key = (
                        float(row["elapsed_s"]),
                        row["phase_label"],
                        row["phase_type"],
                    )
                    per_elapsed[key] = per_elapsed.get(key, 0.0) + float(row["value"])
                points = [
                    {
                        "elapsed_s": elapsed,
                        "phase_label": phase_label,
                        "phase_type": phase_type,
                        "value": value,
                    }
                    for (elapsed, phase_label, phase_type), value in sorted(per_elapsed.items())
                ]
            else:
                per_elapsed: dict[tuple[float, str, str], list[float]] = {}
                for row in selected:
                    if row["subject_kind"] != "adapter":
                        continue
                    key = (
                        float(row["elapsed_s"]),
                        row["phase_label"],
                        row["phase_type"],
                    )
                    per_elapsed.setdefault(key, []).append(float(row["value"]))
                points = []
                for (elapsed, phase_label, phase_type), values in sorted(per_elapsed.items()):
                    if metric in RATE_METRICS:
                        value = float(sum(values))
                    elif metric in LATENCY_METRICS or metric in DEPTH_METRICS:
                        value = float(max(values))
                    else:
                        value = float(values[-1])
                    points.append(
                        {
                            "elapsed_s": elapsed,
                            "phase_label": phase_label,
                            "phase_type": phase_type,
                            "value": value,
                        }
                    )

            if points:
                out[metric][system] = points
    return out


def _timeline_outliers(
    timeline_data: dict[str, dict[str, list[dict]]],
    systems: list[str],
    phases: list[Phase],
) -> list[dict[str, object]]:
    """Find suspicious adapter samples that are likely to warp timeline plots."""
    flagged: list[dict[str, object]] = []
    metrics = [
        "completion_rate",
        "enqueue_rate",
        "producer_p99_ms",
        "producer_call_p99_ms",
        "subscriber_p99_ms",
        "end_to_end_p99_ms",
        "claim_p99_ms",
        "queue_depth",
    ]
    for metric in metrics:
        for system in systems:
            points = timeline_data.get(metric, {}).get(system, [])
            for phase in phases:
                phase_points = [pt for pt in points if pt["phase_label"] == phase.label]
                if len(phase_points) < 5:
                    continue
                values = [float(pt["value"]) for pt in phase_points]
                median = statistics.median(values)
                deviations = [abs(v - median) for v in values]
                mad = statistics.median(deviations)
                floor = max(1.0, abs(median) * 0.5)
                for pt in phase_points:
                    value = float(pt["value"])
                    delta = abs(value - median)
                    if mad > 0:
                        is_outlier = delta > 8 * mad and delta > floor
                    else:
                        is_outlier = delta > max(10.0, abs(median) * 3.0)
                    if is_outlier:
                        flagged.append(
                            {
                                "system": system,
                                "metric": metric,
                                "phase_label": phase.label,
                                "elapsed_s": float(pt["elapsed_s"]),
                                "value": value,
                                "median": median,
                            }
                        )
    flagged.sort(key=lambda item: abs(float(item["value"]) - float(item["median"])), reverse=True)
    return flagged[:12]


def _outlier_panel(outliers: list[dict[str, object]], system_meta: dict[str, dict[str, str]]) -> str:
    if not outliers:
        return (
            "<div class='card'>"
            "<div class='card-title'>Sample Quality</div>"
            "<div class='card-sub'>No suspicious adapter-metric outliers detected in this bundle.</div>"
            "</div>"
        )

    items = []
    for outlier in outliers:
        system = str(outlier["system"])
        items.append(
            "<li><strong>{system}</strong> {metric} in <code>{phase}</code> at {elapsed:.1f}s: "
            "{value:.3f} vs median {median:.3f}</li>".format(
                system=html.escape(system_meta.get(system, {}).get("display_name", system)),
                metric=html.escape(METRIC_LABELS.get(str(outlier["metric"]), str(outlier["metric"]))),
                phase=html.escape(str(outlier["phase_label"])),
                elapsed=float(outlier["elapsed_s"]),
                value=float(outlier["value"]),
                median=float(outlier["median"]),
            )
        )
    return (
        "<div class='card'>"
        "<div class='card-title'>Sample Quality</div>"
        "<div class='card-sub'>Potential graph-warping samples detected. Check raw.csv before trusting the exact timeline shape.</div>"
        f"<ul class='outlier-list'>{''.join(items)}</ul>"
        "</div>"
    )


def _phase_table(summary: dict, systems: list[str], phases: list[Phase], system_meta: dict[str, dict[str, str]]) -> str:
    headers = [
        "System",
        "Phase",
        "Type",
        "Throughput/s",
        "Producer p99 ms",
        "Producer call p99 ms",
        "Subscriber p99 ms",
        "End-to-end p99 ms",
        "Median dead tuples",
        "Peak dead tuples",
    ]
    rows: list[str] = []
    for system in systems:
        system_block = summary["systems"].get(system, {})
        phase_map = system_block.get("phases", {})
        for phase in phases:
            if phase.type.value == "warmup":
                continue
            block = phase_map.get(phase.label)
            if not block:
                continue
            rows.append(
                "<tr>"
                f"<td>{html.escape(system_meta[system]['display_name'])}</td>"
                f"<td>{html.escape(phase.label)}</td>"
                f"<td>{html.escape(block.get('phase_type', ''))}</td>"
                f"<td data-sort='{block.get('median_throughput_per_s') or 0}'>{(block.get('median_throughput_per_s') or 0):.1f}</td>"
                f"<td data-sort='{block.get('median_producer_p99_ms') or 0}'>{(block.get('median_producer_p99_ms') or 0):.3f}</td>"
                f"<td data-sort='{block.get('median_producer_call_p99_ms') or 0}'>{(block.get('median_producer_call_p99_ms') or 0):.3f}</td>"
                f"<td data-sort='{block.get('median_subscriber_p99_ms') or 0}'>{(block.get('median_subscriber_p99_ms') or 0):.3f}</td>"
                f"<td data-sort='{block.get('median_end_to_end_p99_ms') or 0}'>{(block.get('median_end_to_end_p99_ms') or 0):.3f}</td>"
                f"<td data-sort='{block.get('median_dead_tup') or 0}'>{(block.get('median_dead_tup') or 0):.0f}</td>"
                f"<td data-sort='{block.get('peak_dead_tup') or 0}'>{(block.get('peak_dead_tup') or 0):.0f}</td>"
                "</tr>"
            )
    head = "".join(f"<th data-sortable='true'>{html.escape(h)}</th>" for h in headers)
    return (
        "<table id='phase-summary-table'>"
        f"<thead><tr>{head}</tr></thead>"
        f"<tbody>{''.join(rows)}</tbody>"
        "</table>"
    )


def _replica_table(summary: dict, systems: list[str], phases: list[Phase], system_meta: dict[str, dict[str, str]]) -> str:
    headers = [
        "System",
        "Replica",
        "Phase",
        "Completion/s",
        "Enqueue/s",
        "Subscriber p99 ms",
        "End-to-end p99 ms",
        "Queue depth",
        "Running depth",
    ]
    rows: list[str] = []
    for system in systems:
        phase_map = summary["systems"].get(system, {}).get("phases", {})
        for phase in phases:
            if phase.type.value == "warmup":
                continue
            block = phase_map.get(phase.label)
            if not block:
                continue
            replicas = block.get("replicas", {})
            for replica_id, replica_block in sorted(replicas.items(), key=lambda item: int(item[0])):
                rows.append(
                    "<tr>"
                    f"<td>{html.escape(system_meta[system]['display_name'])}</td>"
                    f"<td>{html.escape(str(replica_id))}</td>"
                    f"<td>{html.escape(phase.label)}</td>"
                    f"<td>{(replica_block.get('median_completion_rate') or 0):.1f}</td>"
                    f"<td>{(replica_block.get('median_enqueue_rate') or 0):.1f}</td>"
                    f"<td>{(replica_block.get('median_subscriber_p99_ms') or 0):.3f}</td>"
                    f"<td>{(replica_block.get('median_end_to_end_p99_ms') or 0):.3f}</td>"
                    f"<td>{(replica_block.get('median_queue_depth') or 0):.1f}</td>"
                    f"<td>{(replica_block.get('median_running_depth') or 0):.1f}</td>"
                    "</tr>"
                )
    return (
        "<table id='replica-summary-table'>"
        f"<thead><tr>{''.join(f'<th>{html.escape(h)}</th>' for h in headers)}</tr></thead>"
        f"<tbody>{''.join(rows)}</tbody>"
        "</table>"
    )


def _plot_gallery(run_dir: Path) -> str:
    cards: list[str] = []
    for stem in PLOT_ORDER:
        svg = run_dir / "plots" / f"{stem}.svg"
        if not svg.exists():
            continue
        title = stem.replace("_", " ").title()
        rel = f"plots/{svg.name}"
        cards.append(
            "<button class='plot-card' data-plot-src='{src}' data-plot-title='{title}'>"
            "<div class='plot-card-title'>{title}</div>"
            "<img src='{src}' alt='{title}' loading='lazy' />"
            "</button>".format(src=html.escape(rel), title=html.escape(title))
        )
    return "<div class='plot-grid'>" + "".join(cards) + "</div>"


